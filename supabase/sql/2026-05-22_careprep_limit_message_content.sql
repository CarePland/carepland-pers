insert into public.app_content_versions (
  content_key,
  label,
  description,
  body,
  version_number,
  is_current,
  change_note,
  content_hash
)
select
  seeded.content_key,
  seeded.label,
  seeded.description,
  seeded.body,
  1,
  true,
  'Initial manual CarePrep limit message',
  md5(seeded.content_key || '|' || seeded.label || '|' || seeded.description || '|' || seeded.body)
from (
  values
    (
      'careprep_manual_limit_message',
      'Manual CarePrep limit message',
      'Message shown when a user has used the current plan allowance for manual CarePrep generations.',
      $$You have used this month's manual CarePrep generations. Plan changes are not wired up yet, but support can help during beta.$$
    )
) as seeded(content_key, label, description, body)
where not exists (
  select 1
  from public.app_content_versions existing
  where existing.content_key = seeded.content_key
);
